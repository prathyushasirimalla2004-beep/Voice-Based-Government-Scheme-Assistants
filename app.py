def detect_intent(text):
    t = text.lower()
    if any(w in t for w in ["hello","hi","నమస్","హలో","start","welcome","నమస్కారం","hey"]):
        return "greet"
    if any(w in t for w in ["అన్ని","list","అన్నీ","all schemes","అన్ని పథకాలు","చూపించు","show all","show me"]):
        return "list"
    if any(w in t for w in ["help","సహాయం","ఏమి అడగాలి","what can","what to ask"]):
        return "help"
    return "search"

def find_scheme(query):
    q = query.lower()
    best_name, best_data, best_score = None, None, 0
    for name, data in SCHEMES.items():
        score = 0
        for kw in data["keywords"]:
            if kw in q:
                score += 2
            elif any(part in q for part in kw.split() if len(part) > 3):
                score += 1
        # exact name boost
        if name.lower() in q: score += 8
        # partial name boost
        for word in name.lower().split():
            if len(word) > 3 and word in q:
                score += 3
        if score > best_score:
            best_score = score
            best_name = name
            best_data = data
    return (best_name, best_data) if best_score > 0 else (None, None)

