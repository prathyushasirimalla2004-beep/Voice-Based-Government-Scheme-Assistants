import os as _os

_DATASET_DIRS = [
    _os.path.join(_os.path.dirname(_os.path.abspath(__file__)), "dataset", "andhra-pradesh"),
    _os.path.join(_os.path.dirname(_os.path.abspath(__file__)), "dataset"),
]
_DOCS_CACHE = []

def load_docs():
    global _DOCS_CACHE
    if _DOCS_CACHE:
        return _DOCS_CACHE
    for d in _DATASET_DIRS:
        if not _os.path.exists(d):
            continue
        for fname in sorted(_os.listdir(d)):
            if not fname.endswith(".txt"):
                continue
            try:
                txt = open(_os.path.join(d, fname), encoding="utf-8", errors="ignore").read().strip()
                if len(txt) > 200:
                    _DOCS_CACHE.append(txt)
            except Exception:
                pass
        if _DOCS_CACHE:
            break
    return _DOCS_CACHE

def search_dataset(query, top_n=3):
    docs = load_docs()
    q = query.lower()
    words = [w for w in re.split(r'\s+', q) if len(w) > 2]
    scored = []
    for doc in docs:
        dl = doc.lower()
        score = sum(2 if w in dl[:400] else 1 for w in words if w in dl)
        if score > 0:
            scored.append((score, doc))
    scored.sort(reverse=True)
    return [d for _, d in scored[:top_n]]

